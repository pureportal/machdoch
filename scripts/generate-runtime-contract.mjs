const getEnum = (name) => {
  const values = defs[name]?.enum;

  if (!Array.isArray(values) || values.some((value) => typeof value !== "string")) {
    throw new Error(`Expected $defs.${name}.enum to be a string enum.`);
  }

  return values;
};

const getObjectProperties = (name) => {
  const properties = defs[name]?.properties;

  if (!properties || typeof properties !== "object" || Array.isArray(properties)) {
    throw new Error(`Expected $defs.${name}.properties to be an object.`);
  }

  return properties;
};

const getDefaultObject = (name) => {
  const defaults = {};

  for (const [key, property] of Object.entries(getObjectProperties(name))) {
    if (Object.prototype.hasOwnProperty.call(property, "default")) {
      defaults[key] = property.default;
    }
  }

  return defaults;
};

const getBoundsObject = (name) => {
  const bounds = {};

  for (const [key, property] of Object.entries(getObjectProperties(name))) {
    if (
      Object.prototype.hasOwnProperty.call(property, "minimum") ||
      Object.prototype.hasOwnProperty.call(property, "maximum")
    ) {
      bounds[key] = {
        ...(Object.prototype.hasOwnProperty.call(property, "minimum")
          ? { min: property.minimum }
          : {}),
        ...(Object.prototype.hasOwnProperty.call(property, "maximum")
          ? { max: property.maximum }
          : {}),
      };
    }
  }

  return bounds;
};

const pascalToScreamingSnake = (value) =>
  value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[-\s]+/g, "_")
    .toUpperCase();

const json = (value) => JSON.stringify(value, null, 2);

const tsReadonlyArray = (values) => `[${values.map((value) => json(value)).join(", ")}]`;

const tsStringUnion = (values) => values.map((value) => json(value)).join(" | ");

const rustStringArray = (values) =>
  `[${values.map((value) => JSON.stringify(value)).join(", ")}]`;

const rustPairs = (record) =>
  `[${Object.entries(record)
    .map(([key, value]) => `(${JSON.stringify(key)}, ${JSON.stringify(value)})`)
    .join(", ")}]`;

const rustConstName = (prefix, propertyName) =>
  `${prefix}_${pascalToScreamingSnake(propertyName)}`;

