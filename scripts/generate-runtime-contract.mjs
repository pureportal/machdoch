const rustNumericConst = (name, value, forceFloat = false) => {
  if (!forceFloat && Number.isInteger(value)) {
    return `pub const ${name}: u32 = ${value};`;
  }

  const literal = Number.isInteger(value) ? `${value}.0` : `${value}`;

  return `pub const ${name}: f64 = ${literal};`;
};

const rustDefaultDesktopConstants = Object.entries(defaultDesktopSettings)
  .map(([key, value]) => {
    const constName = rustConstName("DEFAULT_DESKTOP_SETTING", key);
    const forceFloat = getObjectProperties("UserDesktopSettings")[key]?.type === "number";

    if (typeof value === "boolean") {
      return `pub const ${constName}: bool = ${value};`;
    }

    if (typeof value === "number") {
      return rustNumericConst(constName, value, forceFloat);
    }

    return `pub const ${constName}: &str = ${JSON.stringify(value)};`;
  })
  .join("\n");

const rustDesktopBoundsConstants = Object.entries(desktopSettingBounds)
  .flatMap(([key, value]) => {
    const prefix = rustConstName("DESKTOP_SETTING", key);
    const forceFloat = getObjectProperties("UserDesktopSettings")[key]?.type === "number";
    const constants = [];

    if (value.min !== undefined) {
      constants.push(rustNumericConst(`MIN_${prefix}`, value.min, forceFloat));
    }

    if (value.max !== undefined) {
      constants.push(rustNumericConst(`MAX_${prefix}`, value.max, forceFloat));
    }

    return constants;
  })
  .join("\n");

